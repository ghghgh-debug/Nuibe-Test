const { createClient } = require('@supabase/supabase-js');
require('dotenv').config();

const SUPABASE_URL = process.env.SUPABASE_URL || process.env.NEXT_PUBLIC_SUPABASE_URL;
const SUPABASE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY || process.env.SUPABASE_KEY;

if (!SUPABASE_URL || !SUPABASE_KEY) {
  console.error('Missing SUPABASE_URL or SUPABASE_KEY in environment variables.');
  process.exit(1);
}

const supabase = createClient(SUPABASE_URL, SUPABASE_KEY);

const SCHEMA_SQL = `
-- Run this in your Supabase SQL Editor:

CREATE TABLE IF NOT EXISTS products (
  id TEXT PRIMARY KEY,
  title TEXT NOT NULL,
  price REAL NOT NULL,
  category TEXT,
  detail TEXT,
  image TEXT,
  model TEXT,
  gallery JSONB,
  featured TEXT,
  tags JSONB,
  actives JSONB,
  description TEXT,
  sort_order INTEGER DEFAULT 0,
  updated_at BIGINT
);

CREATE TABLE IF NOT EXISTS faqs (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  lang TEXT NOT NULL,
  question TEXT NOT NULL,
  answer TEXT NOT NULL,
  category TEXT
);

CREATE TABLE IF NOT EXISTS orders (
  id TEXT PRIMARY KEY,
  telegram_id TEXT,
  telegram_username TEXT,
  customer_name TEXT,
  customer_phone TEXT,
  customer_address TEXT,
  delivery_time TEXT,
  notes TEXT,
  created_at BIGINT,
  total REAL,
  status TEXT,
  items JSONB
);

CREATE TABLE IF NOT EXISTS admins (
  telegram_id TEXT PRIMARY KEY,
  name TEXT,
  created_at BIGINT
);
`;

async function seed() {
  console.log('--- Supabase Setup ---');
  console.log('1. Make sure you have created the tables. If not, run this SQL in the Supabase SQL Editor:');
  console.log(SCHEMA_SQL);
  console.log('\n2. Attempting to seed initial data...');

  try {
    const products = require('./seed-products');
    console.log(`Found ${products.length} products to seed.`);
    for (const p of products) {
      const { error } = await supabase.from('products').upsert({
        ...p,
        gallery: p.gallery || [],
        tags: p.tags || [],
        actives: p.actives || [],
        updated_at: Date.now()
      });
      if (error) {
        if (error.code === 'PGRST204' || error.code === '42P01') {
          console.error('Error: "products" table not found. Did you run the SQL schema yet?');
          return;
        }
        console.error(`Error seeding product ${p.id}:`, error.message);
      } else {
        console.log(`Seeded product: ${p.id}`);
      }
    }

    const faqs = require('./seed-faqs');
    console.log(`Found ${faqs.length} FAQs to seed.`);
    for (const f of faqs) {
      const { error } = await supabase.from('faqs').upsert(f);
      if (error) console.error(`Error seeding FAQ "${f.question.slice(0, 20)}...":`, error.message);
      else console.log(`Seeded FAQ: ${f.question.slice(0, 20)}...`);
    }

    console.log('\nSeeding complete.');
  } catch (err) {
    console.error('Seeding failed:', err.message);
  }
}

seed();
